        <h3>🎯 SP 5.1.1: Mengenal Pasti Pemboleh Ubah dan Nilainya</h3>
        <p>💡 <strong>Standard Pembelajaran (SP)</strong></p>
        <p>
            Menggunakan huruf untuk mewakilkan kuantiti yang tidak diketahui
            nilai. Seterusnya menyatakan sama ada pemboleh ubah itu mempunyai
            nilai yang tetap atau nilai yang berubah dengan memberi justifikasi.
        </p>
        <p>👉 <strong>Apa Itu Pemboleh Ubah?</strong></p>
        <ul>
            <li>
                📌 <strong>Pemboleh ubah:</strong> Huruf yang mewakili nilai berubah.
            </li>
            <li>
                🎩 <strong>Pekali:</strong> Nombor yang mengiringi pemboleh ubah
                dalam suatu ungkapan algebra.
            </li>
        </ul>
        <p>📚 <strong>Contoh:</strong></p>
        <ul>
            <li>
                ✅ <strong>𝑥 + 3</strong> → 𝑥 ialah pemboleh ubah, 3 ialah pemalar.
            </li>
            <li>
                ✅ <strong>7𝑦 - 2</strong> → 𝑦 ialah pemboleh ubah, 7 ialah pekali.
            </li>
        </ul>
