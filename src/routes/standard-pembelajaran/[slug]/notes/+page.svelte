        <h3>🎯 SP 5.1.5: Mengenal Pasti Sebutan Serupa dan Tidak Serupa</h3>
        <p>💡 <strong>Standard Pembelajaran (SP)</strong></p>
        <p>Mengenal pasti sebutan serupa dan sebutan tidak serupa.</p>
        <p>🔄 <strong>Definisi:</strong></p>
        <ul>
            <li>
                ✅ <strong>Sebutan Serupa:</strong> Mempunyai pemboleh ubah yang
                sama dengan kuasa yang sama.
            </li>
            <li>
                ✅ <strong>Sebutan Tidak Serupa:</strong> Mempunyai pemboleh ubah
                yang berbeza atau kuasa yang berbeza.
            </li>
        </ul>
        <p>📚 <strong>Contoh:</strong></p>
        <ul>
            <li>📘 <strong>2𝑥 dan 5𝑥</strong> → Serupa ✅</li>
            <li>📘 <strong>3𝑎 dan 4𝑏</strong> → Tidak Serupa ❌</li>
            <li>📘 <strong>7𝑥² dan 𝑥²</strong> → Serupa ✅</li>
            <li>📘 <strong>2𝑥 dan 3𝑦</strong> → Tidak Serupa ❌</li>
        </ul>
