        <h3>🎯 SP 5.1.3: Menentukan Nilai Ungkapan Algebra</h3>
        <p>💡 <strong>Standard Pembelajaran (SP)</strong></p>
        <p>
            Menentukan nilai ungkapan algebra apabila nilai pemboleh ubah diberi
            dan membuat perkaitan dengan situasi yang sesuai.
        </p>
        <p>🔄 <strong>Langkah-langkah:</strong></p>
        <ul>
            <li>✅ Gantikan pemboleh ubah dengan nilai yang diberi.</li>
            <li>✅ Selesaikan operasi matematik.</li>
        </ul>
        <p>📚 <strong>Contoh:</strong></p>
        <ul>
